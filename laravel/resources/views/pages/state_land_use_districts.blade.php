<h2>State Land Use Districts</h2>
<h3>URBAN DISTRICT</h3>
<p>The Urban District generally includes lands characterized by “city-like” concentrations of people, structures and services. This District also includes vacant areas for future development.</p>
<p>Jurisdiction of this district lies primarily with the respective counties. Generally, lot sizes and uses permitted in the district area are established by the respective county through ordinances or rules.</p>
<h3>RURAL DISTRICT</h3>
<p>Rural Districts are composed primarily of small farms intermixed with low-density residential lots with a minimum size of one-half acre.</p>
<p>Jurisdiction over Rural Districts is shared by the Commission and county governments. Permitted uses include those relating or compatible to agricultural use and low-density residential lots.</p>
<p>Variances can by obtained through the special use permitting process.</p>
<h3>AGRICULTURAL DISTRICT</h3>
<p>The Agricultural District includes lands for the cultivation of crops, aquaculture, raising livestock, wind energy facility, timber cultivation, agriculture-support activities (i.e., mills, employee quarters, etc.) and land with significant potential for agriculture uses. Golf courses and golf-related activities approved by a county before July 1, 2005, may be allowed in this district, otherwise such new facilities would be prohibited.</p>
<p>Uses permitted in the highest productivity agricultural categories are governed by statute. Uses in the lower-productivity categories – C, D, E or U &#8211; are established by the Commission and include those allowed on A or B lands as well as those stated under Section 205-4.5, Hawaii Revised Statutes.</p>
<p><a href="http://hdoa.hawaii.gov/chair/new-agriculture-initiatives/important-ag-lands-ial/">Important Agricultural Land (IAL) designation information</a> from State Department of Agriculture (DOA)</p>
<h3>CONSERVATION DISTRICT</h3>
<p>Conservation lands are comprised primarily of lands in existing forest and water reserve zones and include areas necessary for protecting watersheds and water sources, scenic and historic areas, parks, wilderness, open space, recreational areas, habitats of endemic plants, fish and wildlife, and all submerged lands seaward of the shoreline. The conservation District also includes lands subject to flooding and soil erosion.</p>
<p>Conservation Districts are administrated by the State Board of Land and Natural Resources and uses are governed by rules promulgated by the State Department of Land and Natural Resources.</p>
